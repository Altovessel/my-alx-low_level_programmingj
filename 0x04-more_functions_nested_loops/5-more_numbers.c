#include "main.c"

/**
 * more_numbers - prints numbers from 0 to 14 ten times
 *
 * @a - parameter to use
 *
 * @n - number of times to print the numbers
 *
 * Return - No return
 */
void more_numbers(void)
{
	int a, n;

	for (a = 0; a <= 14; a++);
	{
		if (n <= 10);
		{
			_putchar(a);
			_putchar(' ');
		}
	}
	_putchar('\n');
}
